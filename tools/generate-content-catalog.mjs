import { mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const output = join(root, '.vitepress/theme/generated/contentCatalog.ts')

function markdownFiles(directory) {
  return readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const path = join(directory, entry.name)
    return entry.isDirectory() ? markdownFiles(path) : entry.name.endsWith('.md') ? [path] : []
  })
}

function unquote(value) {
  const trimmed = value.trim()
  if ((trimmed.startsWith("'") && trimmed.endsWith("'")) || (trimmed.startsWith('"') && trimmed.endsWith('"'))) {
    return trimmed.slice(1, -1)
  }
  return trimmed
}

function parseFrontmatter(source) {
  const match = source.match(/^---\s*\r?\n([\s\S]*?)\r?\n---(?:\r?\n|$)/)
  if (!match) return { frontmatter: {}, body: source }

  const frontmatter = {}
  const lines = match[1].split(/\r?\n/)
  let section = null
  let listKey = null

  for (const line of lines) {
    const topLevel = line.match(/^([\w-]+):\s*(.*)$/)
    if (topLevel) {
      const [, key, value] = topLevel
      section = null
      listKey = null
      if (value) {
        frontmatter[key] = unquote(value)
      } else if (key === 'tags' || key === 'thumbnail') {
        frontmatter[key] = key === 'tags' ? [] : {}
        section = key
      }
      continue
    }

    const nested = line.match(/^\s+([\w-]+):\s*(.*)$/)
    if (section === 'thumbnail' && nested) {
      const [, key, value] = nested
      listKey = null
      if (value) {
        frontmatter.thumbnail[key] = key === 'order' ? Number(value) : unquote(value)
      } else if (key === 'targets') {
        frontmatter.thumbnail.targets = []
        listKey = key
      }
      continue
    }

    const item = line.match(/^\s*-\s+(.*)$/)
    if (item) {
      if (section === 'tags') frontmatter.tags.push(unquote(item[1]))
      if (section === 'thumbnail' && listKey === 'targets') frontmatter.thumbnail.targets.push(unquote(item[1]))
    }
  }

  return { frontmatter, body: source.slice(match[0].length) }
}

function textFromMarkdown(markdown) {
  const lines = markdown.split(/\r?\n/)
  const title = lines.find((line) => /^#\s+/.test(line))?.replace(/^#\s+/, '').trim() ?? ''
  const titleIndex = lines.findIndex((line) => /^#\s+/.test(line))
  const paragraph = lines.slice(titleIndex + 1).filter((line) => {
    const trimmed = line.trim()
    return trimmed && !trimmed.startsWith('#') && !trimmed.startsWith('```') && !trimmed.startsWith('![')
  })[0] ?? ''

  return {
    title,
    description: paragraph
      .replace(/<[^>]+>/g, '')
      .replace(/!?(?:\[[^\]]*\])\([^)]*\)/g, '')
      .replace(/[*_`]/g, '')
      .trim(),
  }
}

function pageEntry(file) {
  const source = readFileSync(file, 'utf8')
  const { frontmatter, body } = parseFrontmatter(source)
  const inferred = textFromMarkdown(body)
  const relativePath = relative(root, file).replaceAll('\\', '/')

  return [
    `/${relativePath}`,
    {
      title: frontmatter.title || inferred.title || relativePath.replace(/\.md$/, ''),
      description: frontmatter.description || frontmatter.thumbnail?.description || inferred.description,
      relativePath,
      frontmatter,
    },
  ]
}

export function generateContentCatalog() {
  const files = [...markdownFiles(join(root, 'Docs')), join(root, 'index.md')].sort()
  const pages = Object.fromEntries(files.map(pageEntry))
  const source = `// Generated by tools/generate-content-catalog.mjs. Do not edit manually.\n\nexport default ${JSON.stringify(pages, null, 2)} as const\n`

  mkdirSync(dirname(output), { recursive: true })
  writeFileSync(output, source)
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  generateContentCatalog()
}
